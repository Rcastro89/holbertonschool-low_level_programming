#include "3-calc.h"
/**
 * op_add - add numbers
 * @a: number one
 * @b: number two
 * Return: operation result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - add numbers
 * @a: number one
 * @b: number two
 * Return: operation result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - add numbers
 * @a: number one
 * @b: number two
 * Return: operation result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - add numbers
 * @a: number one
 * @b: number two
 * Return: operation result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - add numbers
 * @a: number one
 * @b: number two
 * Return: operation result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
